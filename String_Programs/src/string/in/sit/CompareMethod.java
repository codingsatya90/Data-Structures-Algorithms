package string.in.sit;

public class CompareMethod {

	public static void main(String[] args) {
		String name1 = "Kalu";
		String name2 = "Muna";

		// compareTo  
		if (name1.compareTo(name2) == 0) {
			System.out.println("String are equal");
		} else {
			System.out.println("String are not equal");
		}
	}
}
